# R01. 序列解包基礎與進階技巧
# ============================================================================
# 本節介紹 Python 的序列解包（Sequence Unpacking）語法
# 解包允許將序列（如元組、列表）中的元素直接賦值給多個變數
# 這是 Python 中一個強大且實用的特性，能讓程式碼更簡潔易讀
# ============================================================================

## 1. 基礎序列解包
# --------------------------------------------------------------------------
# 最簡單的解包形式：將序列的元素一一對應賦值給變數
# 變數數量必須與序列長度完全匹配，否則會拋出 ValueError

# 範例：解包元組
p = (4, 5)
x, y = p  # 將元組 p 的兩個元素解包到變數 x 和 y
print(f"解包元組：x = {x}, y = {y}")

# 範例：解包列表
data = ['ACME', 50, 91.1, (2012, 12, 21)]
name, shares, price, date = data
print(f"公司資料：名稱={name}, 股數={shares}, 價格={price}, 日期={date}")

# 巢狀解包：處理包含子序列的複雜結構
name, shares, price, (year, mon, day) = data
print(f"巢狀解包：年={year}, 月={mon}, 日={day}")

## 2. 使用占位符丟棄不需要的值
# --------------------------------------------------------------------------
# 有時我們只需要序列中的部分元素，可以使用下劃線 _ 作為占位符
# _ 是一個慣例，表示這個值不會被使用（雖然實際上仍會被賦值）

_, shares, price, _ = data
print(f"丟棄不需要的值：股數={shares}, 價格={price}")

## 3. 星號運算子 * 處理不定長度序列
# --------------------------------------------------------------------------
# 使用星號 * 可以解包不定長度的序列
# * 會將剩餘的所有元素收集成一個列表
# 這是解包的進階技巧，適用於處理長度不確定的序列

# 範例：丟棄首尾，取中間值
grades = [85, 90, 78, 92, 88]
first, *middle, last = grades
print(f"星號解包：首個={first}, 中間={middle}, 最後={last}")

# 應用：計算平均分（排除最高和最低）
def average_without_extremes(scores):
    if len(scores) < 3:
        return sum(scores) / len(scores) if scores else 0
    sorted_scores = sorted(scores)
    return sum(sorted_scores[1:-1]) / len(sorted_scores[1:-1])

print(f"平均分計算：{average_without_extremes(grades)}")

## 4. 多重賦值與變數交換
# --------------------------------------------------------------------------
# 解包可以用於同時賦值多個變數，或實現變數交換

# 多重賦值
a, b, c = 1, 2, 3
print(f"多重賦值：a={a}, b={b}, c={c}")

# 變數交換（無需臨時變數）
x, y = 10, 20
x, y = y, x
print(f"變數交換：x={x}, y={y}")

## 5. 函數返回值解包
# --------------------------------------------------------------------------
# 函數可以返回多個值（實際上是元組），然後解包到多個變數

def get_user_info():
    """返回用戶的基本資訊"""
    return 'Alice', 25, 'Engineer'

name, age, job = get_user_info()
print(f"函數返回值解包：姓名={name}, 年齡={age}, 職業={job}")

## 6. 解包在迴圈中的應用
# --------------------------------------------------------------------------
# 在 for 迴圈中，解包可以讓我們同時處理多個值

# 基本解包
points = [(1, 2), (3, 4), (5, 6)]
for x, y in points:
    print(f"座標點：({x}, {y})")

# 帶星號的解包
user_data = [('Alice', 25, 'Engineer', 'New York'),
             ('Bob', 30, 'Designer', 'London', 'Remote')]
for name, age, *details in user_data:
    print(f"{name}（{age}歲）的詳細資訊：{details}")

# ============================================================================
# 語法重點總結：
# 1. 變數數量必須匹配序列長度（除非使用 *）
# 2. * 可以收集剩餘元素成列表
# 3. _ 慣例用於丟棄不需要的值
# 4. 適用於元組、列表、字串等序列類型
# 5. 在函數參數和返回值中特別有用
